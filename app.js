/* スコアから達成見込み係数を算出（0.4〜1.0） */
function achievabilityCoef(scores, riskAxisId){
  const axisScore = scores[riskAxisId]||0;
  // 最も影響する軸スコアを主要因として使用（低いほど係数が小さい）
  const totalAvg = Object.values(scores).reduce((a,b)=>a+b,0) / 6;
  const combined = axisScore * 0.6 + totalAvg * 0.4;
  // 0〜60点 → 0.4〜1.0の係数
  return 0.4 + (combined / 60) * 0.6;
}

/* 実現可能性ラベル */
function feasibilityLabel(coef){
  if(coef >= 0.75) return { label:'達成見込み：高',  cls:'sf-high' };
  if(coef >= 0.55) return { label:'達成見込み：中',  cls:'sf-mid'  };
  return                   { label:'達成見込み：要強化',cls:'sf-low'};
}

