function showPg(n){
  document.querySelectorAll('.pg').forEach(p=>p.classList.remove('on'));
  $('pg'+n).classList.add('on');
  window.scrollTo(0,0);
  updatePills(n);
  updateProg();
}

function getSize(){return $('fSz').value||'mid'}
function getInd(){const v=$('fInd').value;return INDUSTRIES[v]?.key||'gen'}
function catQs(cid){const common=QS[cid];const ik=getInd();const extra=(ik!=='gen'&&IND_QS[ik]?IND_QS[ik][cid]||[]:[]);return [...common,...extra]}
function allQsRaw(){return CATS.flatMap(c=>catQs(c.id))}
function isSkipped(q){return q.size_skip&&q.size_skip.includes(getSize())}
function activeQs(){return allQsRaw().filter(q=>!isSkipped(q))}
function updateProg(){
  try{
    const total=activeQs().length;
    const done=Object.keys(ans).length;
    const el=$('pFill');
    if(el) el.style.width=(total?done/total*100:0)+'%';
  }catch(e){/* 初期化前は無視 */}
}
function catScore(cid){
  const active=catQs(cid).filter(q=>!isSkipped(q));
  if(!active.length)return 0;
  const got=active.reduce((s,q)=>s+(ans[q.id]||0),0);
  return Math.round(got/(active.length*5)*60);
}
function getCatLv(sc){return CAT_LV.find(r=>sc>=r.min&&sc<=r.max)||CAT_LV[0]}
function getTotLv(sc){return TOT_LV.find(r=>sc>=r.min&&sc<=r.max)||TOT_LV[0]}

