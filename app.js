/* KPI種別の自動判定 */
function detectKPIType(kpiText){
  // 数値目標を含む場合（%・倍・件・円・万・億・名・時間・日）
  if(/[\d]+\s*[%％倍件円万億名時間日]/.test(kpiText)) return 'numeric';
  // 定性・状態目標
  return 'qualitative';
}

/* KPIのベースライン推定（現状到達率）
   診断スコアの総合レベルから「現状どの程度達成に近いか」を推定 */
function estimateBaseline(scores, kpiText){
  const tot = Object.values(scores).reduce((a,b)=>a+b,0);
  const pct = tot / 360; // 0〜1

  // KPIに関連する軸スコアの平均も加味
  const impact = KPI_AXIS_IMPACT;
  const relAxes = Object.entries(impact).filter(([axId, imp]) =>
    imp.kws.some(w => kpiText.includes(w))
  );
  const relScore = relAxes.length
    ? relAxes.reduce((s,[axId]) => s + (scores[axId]||0), 0) / (relAxes.length * 60)
    : pct;

  // ベースライン：全体スコアと関連軸スコアの加重平均（関連軸を重視）
  return Math.round((pct * 0.4 + relScore * 0.6) * 100);
}

/* フェーズ別改善ポイントの計算
   Phase完了後にロードマップ施策が実施されることでKPIがどれだけ改善するかを推定 */
function calcPhaseGain(scores, kpiText, phase){
  const impact = KPI_AXIS_IMPACT;
  // このKPIに関連する軸を特定
  const relAxes = Object.entries(impact).filter(([axId, imp]) =>
    imp.kws.some(w => kpiText.includes(w))
  );
  if(!relAxes.length) relAxes.push(['org', impact.org]); // デフォルト

  // 各軸のギャップ（改善余地）
  const avgGap = relAxes.reduce((s,[axId]) => s + Math.max(0, 60-(scores[axId]||0)), 0)
    / relAxes.length;
  const gapRatio = avgGap / 60; // 0〜1（ギャップが大きいほど伸び代あり）

  // フェーズごとの基礎改善率（ギャップが大きいほど初期フェーズで大きく改善）
  const baseGains = {
    1: gapRatio > 0.6 ? 8  : gapRatio > 0.3 ? 5  : 3,  // Phase1:基盤整備
    2: gapRatio > 0.6 ? 14 : gapRatio > 0.3 ? 11 : 7,  // Phase2:本格構築
    3: gapRatio > 0.6 ? 12 : gapRatio > 0.3 ? 10 : 8,  // Phase3:最適化
  };
  return baseGains[phase] || 5;
}

