/* ---- PDF出力メイン ---- */
async function dlPDF(type='diag'){
  $('pdfOv').classList.add('on');
  await new Promise(r=>setTimeout(r,100));
  try{
    /* 現在のスコア・プロフィールを取得 */
    const scores={};CATS.forEach(c=>{scores[c.id]=catScore(c.id)});
    const profile={
      company:$('fComp').value.trim(),
      ind:$('fInd').value,
      sz:$('fSz').options[$('fSz').selectedIndex]?.text||'',
      it:$('fIT').value,
      ai:$('fAI').value,
      dt:$('fDt').value||new Date().toISOString().split('T')[0],
    };

    /* PDF専用HTMLを生成 */
    let pdfHtml;
    if(type==='strategy'){
      const kpiRiskData=window._lastKpiRiskData||[];
      pdfHtml = buildStrategyPdfHtml(scores, window._lastPlan||{}, kpiRiskData);
    } else {
      window._ans = ans; // 診断回答をグローバルに渡す
      pdfHtml = buildDiagPdfHtml(scores, profile);
    }

    /* 専用コンテナをbodyに追加（画面外） */
    const container = document.createElement('div');
    container.id = 'pdfContainer';
    container.style.cssText = 'position:fixed;left:-9999px;top:0;z-index:-1;width:794px;background:#fff';
    container.innerHTML = pdfHtml;
    document.body.appendChild(container);

    /* レンダリング完了を待つ */
    await new Promise(r=>setTimeout(r,400));

    /* html2canvas でキャプチャ */
    const fullCanvas = await html2canvas(container, {
      scale: 2,
      useCORS: true,
      backgroundColor: '#ffffff',
      logging: false,
      width: 794,
      windowWidth: 794,
    });

    /* jsPDF でページ分割 */
    const {jsPDF} = window.jspdf;
    const pdf  = new jsPDF({orientation:'portrait', unit:'mm', format:'a4'});
    const PW   = pdf.internal.pageSize.getWidth();   // 210mm
    const PH   = pdf.internal.pageSize.getHeight();  // 297mm
    const MG   = 10;
    const UW   = PW - MG*2;  // 190mm
    const UH   = PH - MG*2;  // 277mm

    const pxPerMm = fullCanvas.width / UW;
    const pageHPx = Math.floor(UH * pxPerMm);

    /* セクション境界のbreakpointsを収集 */
    const breakpoints = new Set([0]);
    container.querySelectorAll([
      'div[style*="padding:28px"]',  // hero
      'div[style*="padding:32px"]',  // hero alt
      'div[style*="margin-bottom:14px"]', // card
      'div[style*="margin-bottom:16px"]', // card alt
    ].join(',')).forEach(el=>{
      const top = el.offsetTop;
      if(top > 4) breakpoints.add(Math.round(top * pxPerMm));
    });
    breakpoints.add(fullCanvas.height);

    const sorted = [...breakpoints].sort((a,b)=>a-b);
    let startPx = 0, first = true;

    while(startPx < fullCanvas.height){
      if(!first) pdf.addPage();
      const endPx = Math.min(startPx + pageHPx, fullCanvas.height);
      let cutPx = endPx;
      for(const bp of sorted){
        if(bp > startPx && bp <= endPx) cutPx = bp;
      }
      if(cutPx <= startPx) cutPx = endPx;

      const sliceH = Math.round(cutPx - startPx);
      if(sliceH <= 0){ startPx = cutPx; first = false; continue; }

      const sl = document.createElement('canvas');
      sl.width = fullCanvas.width; sl.height = sliceH;
      sl.getContext('2d').drawImage(
        fullCanvas, 0, Math.round(startPx), fullCanvas.width, sliceH,
        0, 0, sl.width, sliceH
      );
      pdf.addImage(sl.toDataURL('image/png'), 'PNG', MG, MG, UW, sliceH/pxPerMm);
      startPx = cutPx; first = false;
    }

    const co  = profile.company || '貴社';
    const dt  = profile.dt;
    const sfx = type==='strategy' ? 'AI活用戦略' : '診断レポート';
    pdf.save(`AI-Ready_${sfx}_${co}_${dt}.pdf`);

  }catch(e){
    console.error(e);
    alert('PDF生成中にエラーが発生しました。\n'+e.message);
  }finally{
    /* 専用コンテナを除去 */
    const c = document.getElementById('pdfContainer');
    if(c) c.remove();
    $('pdfOv').classList.remove('on');
  }
}

