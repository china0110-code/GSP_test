function goResults(){
  const unanswered=activeQs().filter(q=>ans[q.id]===undefined);
  if(unanswered.length>0){
    $('e2msg').textContent=`未回答の設問が ${unanswered.length} 問あります。すべての設問に回答してから結果を確認してください。`;
    $('e2').classList.add('on');
    unanswered.forEach(q=>{const c=document.getElementById('qc_'+q.id);if(c)c.classList.add('err')});
    const fi=CATS.findIndex(c=>catQs(c.id).some(q=>!isSkipped(q)&&ans[q.id]===undefined));
    if(fi!==curCat)switchCat(fi);
    return;
  }
  buildResults();showPg(3);
}

function buildResults(){
  const scores={};CATS.forEach(c=>{scores[c.id]=catScore(c.id)});
  const total=Object.values(scores).reduce((a,b)=>a+b,0);
  const tLv=getTotLv(total);
  const sz=getSize();
  const co=$('fComp').value.trim();

  $('rCo').textContent=(co||'貴社')+' — AI-Ready診断レポート';
  $('rLv').textContent=tLv.lv;
  $('rSl').textContent=tLv.sl;
  $('rSc').textContent=total;
  $('rDn').textContent='/ 360';
  $('rCmt').textContent=TOT_CMTS[tLv.lv];

  const dt=$('fDt').value||new Date().toISOString().split('T')[0];
  const indLabel=$('fInd').value||'汎用';
  $('rChips').innerHTML=[
    ['業種',indLabel],['規模',$('fSz').options[$('fSz').selectedIndex]?.text],
    ['IT基盤',$('fIT').value],['AI取り組み',$('fAI').value],['診断日',dt],
  ].map(([k,v])=>`<div class="r-chip"><strong>${k}</strong>${v||'—'}</div>`).join('');

  // Radar (6軸)
  if(radarC){radarC.destroy();radarC=null}
  radarC=new Chart($('radar').getContext('2d'),{
    type:'radar',
    data:{
      labels:CATS.map(c=>c.name),
      datasets:[{
        label:'スコア',data:CATS.map(c=>scores[c.id]),
        backgroundColor:'rgba(184,146,42,.12)',
        borderColor:'rgba(184,146,42,.85)',
        borderWidth:2,pointBackgroundColor:'rgba(184,146,42,1)',pointRadius:5,
      }]
    },
    options:{
      responsive:true,
      scales:{r:{min:0,max:60,
        ticks:{stepSize:15,font:{size:10},color:'#7a849a'},
        grid:{color:'rgba(0,0,0,.07)'},angleLines:{color:'rgba(0,0,0,.07)'},
        pointLabels:{font:{size:12,family:"'Noto Sans JP',sans-serif",weight:'700'},color:'#0b1628'},
      }},
      plugins:{legend:{display:false}}
    }
  });

  // Category cards
  const barCols=CATS.map(c=>c.color);
  const szMsg=SIZE_MSGS[sz];
  $('catGrid').innerHTML=CATS.map((c,i)=>{
    const sc=scores[c.id];
    const lv=getCatLv(sc);
    const pct=Math.round(sc/60*100);
    const cmt=CMTS[c.id][lv.lv]||'';
    const skippedN=catQs(c.id).filter(q=>isSkipped(q)).length;
    const adjNote=skippedN>0&&szMsg?.note?`<div class="cg-adj"><span>ℹ</span><span>${skippedN}問スキップ・按分換算済み</span></div>`:'';
    return `<div class="cg-card au au${i+1}">
      <div class="cg-label">${c.name}</div>
      <div class="cg-level">${lv.lv} — ${lv.lb}</div>
      <div class="bar-track"><div class="bar-fill" style="width:${pct}%;background:${barCols[i]}"></div></div>
      <div class="cg-scores"><span class="cg-val">${sc}</span><span class="cg-max">/ 60 点（換算後）</span></div>
      <div class="cg-div"></div>
      <div class="cg-cmt">${cmt}</div>
      ${adjNote}
    </div>`;
  }).join('');

  // Priority table
  const catMap={};CATS.forEach(c=>catQs(c.id).forEach(q=>{catMap[q.id]=c.name}));
  const ranked=activeQs()
    .map(q=>({...q,sc:ans[q.id]||0,gap:5-(ans[q.id]||0),cat:catMap[q.id]}))
    .filter(q=>q.gap>0)
    .sort((a,b)=>b.gap-a.gap)
    .slice(0,15);

  const optL=v=>OPTS.find(o=>o.v===v)?.l||'—';
  const priB=g=>g>=3?'<span class="badge b-hi">高</span>':g>=2?'<span class="badge b-md">中</span>':'<span class="badge b-lo">低</span>';
  const indMark=q=>q.ind?'<span style="font-size:.65rem;background:#e0f2fe;color:#0369a1;border-radius:3px;padding:1px 6px;margin-left:6px;font-weight:700">業界固有</span>':'';

  $('priBody').innerHTML=ranked.length?ranked.map(q=>`
    <tr>
      <td>${priB(q.gap)}</td>
      <td style="white-space:nowrap">${q.cat}</td>
      <td><div class="q-ct">${q.t}${indMark(q)}</div><div class="q-ct2">${q.q}</div></td>
      <td style="white-space:nowrap">${optL(q.sc)}</td>
      <td><span class="gap-v">▲${q.gap}</span></td>
    </tr>`).join(''):
    '<tr><td colspan="5" style="text-align:center;color:var(--muted);padding:24px">すべての設問で最高評価です。</td></tr>';
}

