function copyPrompt(){
  const txt=$('promptText').textContent;
  navigator.clipboard.writeText(txt).then(()=>{
    const btn=$('copyBtn');
    btn.textContent='コピー済み ✓';btn.classList.add('copied');
    setTimeout(()=>{btn.textContent='コピー';btn.classList.remove('copied')},2500);
  }).catch(()=>{
    const ta=document.createElement('textarea');
    ta.value=txt;document.body.appendChild(ta);ta.select();
    document.execCommand('copy');document.body.removeChild(ta);
    const btn=$('copyBtn');
    btn.textContent='コピー済み ✓';btn.classList.add('copied');
    setTimeout(()=>{btn.textContent='コピー';btn.classList.remove('copied')},2500);
  });
  updateSfFlow(2);
}

function onPasteInput(){
  const raw=$('jsonPaste').value.trim();
  if(!raw){$('jsonPaste').className='paste-area';$('sfCardC').style.display='none';parsedPlan=null;return}
  try{
    // ```json ... ``` のフェンスを除去
    const cleaned=raw.replace(/^```json\s*/,'').replace(/^```\s*/,'').replace(/\s*```$/,'').trim();
    const obj=JSON.parse(cleaned);
    parsedPlan=obj;
    $('jsonPaste').className='paste-area ok';
    $('eJson').classList.remove('on');
    renderConfirmForm(obj);
    $('sfCardC').style.display='block';
    updateSfFlow(3);
  }catch(e){
    parsedPlan=null;
    $('jsonPaste').className='paste-area ng';
    $('eJson').classList.add('on');
    $('eJsonMsg').textContent='JSONの形式が正しくありません。Claude.aiの出力をそのまま（```を含めても可）貼り付けてください。';
    $('sfCardC').style.display='none';
  }
}

