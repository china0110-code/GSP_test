function renderTabs(){
  $('ctabs').innerHTML=CATS.map((c,i)=>{
    const active=catQs(c.id).filter(q=>!isSkipped(q));
    const done=active.filter(q=>ans[q.id]!==undefined).length;
    const full=done===active.length&&active.length>0;
    return `<button class="cat-tab ${i===curCat?'act':''} ${full?'done':''}" onclick="switchCat(${i})">
      ${c.name}<span class="tc">${done}/${active.length}</span>
    </button>`;
  }).join('');
}

function renderIndBanner(){
  const ik=getInd();
  if(ik==='gen'){$('indBanner').innerHTML='';return}
  const ind=Object.values(INDUSTRIES).find(v=>v.key===ik);
  if(!ind){$('indBanner').innerHTML='';return}
  const extraTotal=Object.values(IND_QS[ik]).reduce((s,a)=>s+a.length,0);
  $('indBanner').innerHTML=`<div class="ind-banner">
    <div class="ind-banner-left">
      <span class="ind-banner-icon">${ind.icon}</span>
      <div class="ind-banner-text">
        <strong>${ind.label}向け追加設問が適用されています</strong>
        <span>${ind.desc}</span>
      </div>
    </div>
    <div class="ind-banner-right">追加 ${extraTotal} 問（全6軸）</div>
  </div>`;
}

function switchCat(i, scrollToTop=true){
  curCat=i;
  const c=CATS[i];
  $('qEye').textContent=`AXIS ${i+1} / 6`;
  $('qTitle').textContent=c.name;
  $('qDesc').textContent=c.desc;
  renderSizeNotice(c.id);
  renderQs(c);
  renderTabs();
  $('e2').classList.remove('on');
  /* カテゴリ切り替え時は常にヘッダー位置へスクロール */
  if(scrollToTop){
    const hdr=document.getElementById('qCatHdr');
    const top = hdr
      ? hdr.getBoundingClientRect().top + window.pageYOffset - 120
      : 0;
    window.scrollTo({top: Math.max(0, top), behavior:'smooth'});
  }
}

function renderSizeNotice(cid){
  const sz=getSize();
  const msg=SIZE_MSGS[sz];
  const skipped=catQs(cid).filter(q=>isSkipped(q)).length;
  const note=msg?.note||'';
  if(skipped>0&&note){
    $('sizeNotice').innerHTML=`<div class="size-notice"><span class="size-notice-icon">ℹ</span><span>${note}（このカテゴリで <strong>${skipped}問</strong> がスキップされています）</span></div>`;
  } else {
    $('sizeNotice').innerHTML='';
  }
}

function renderQs(c){
  $('qWrap').innerHTML=catQs(c.id).map((q,qi)=>{
    const skip=isSkipped(q);
    const adj=q.size_adjust&&q.size_adjust[getSize()];
    const isInd=q.ind===true;
    const v=ans[q.id];
    if(skip){
      return `<div class="q-card" style="opacity:.38;pointer-events:none">
        <div class="q-top">
          <div class="q-num">${qi+1}</div>
          <div><div class="q-title">${q.t} <span class="adj-tag">貴社規模では対象外</span></div></div>
        </div>
      </div>`;
    }
    return `<div class="q-card ${v!==undefined?'ans':''} ${adj?'adj':''} ${isInd?'ind-q':''}" id="qc_${q.id}">
      <div class="q-top">
        <div class="q-num ${adj?'adj-mark':''} ${isInd?'':''}">${qi+1}</div>
        <div style="flex:1">
          <div class="q-title">【${q.t}】 ${q.q}${isInd?'<span class="ind-tag">業界固有</span>':''}${adj?'<span class="adj-tag">規模別補足あり</span>':''}</div>
          <div class="q-hint">${q.h}</div>
          ${adj?`<div class="q-hint" style="color:#7c3aed;margin-top:4px">📌 ${adj}</div>`:''}
        </div>
      </div>
      <div class="q-opts">
        ${OPTS.map(o=>`<label><input type="radio" name="${q.id}" value="${o.v}" ${v==o.v?'checked':''} onchange="selAns('${q.id}',${o.v})"><div class="opt-box">${o.l}</div></label>`).join('')}
      </div>
    </div>`;
  }).join('');
}

function selAns(qid,val){
  ans[qid]=val;
  const c=document.getElementById('qc_'+qid);
  if(c){c.classList.add('ans');c.classList.remove('err')}
  renderTabs();updateProg();

  /* 現在カテゴリの全問が埋まったら自動で次へ */
  const cat=CATS[curCat];
  const activeCatQs=catQs(cat.id).filter(q=>!isSkipped(q));
  const allAnswered=activeCatQs.every(q=>ans[q.id]!==undefined);
  if(allAnswered){
    const nextIdx=curCat+1;
    if(nextIdx<CATS.length){
      setTimeout(()=>{ switchCat(nextIdx, true); }, 320);
    }
  }
}

