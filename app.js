/* リスクスコア計算（0〜100） */
function calcAxisRisk(axisId, score, kpiText){
  const impact = KPI_AXIS_IMPACT[axisId];
  if(!impact) return {riskScore:0, relevance:false};
  // KPIテキストとのキーワード一致数
  const matchCount = impact.kws.filter(w => kpiText.includes(w)).length;
  const relevance = matchCount > 0;
  // スコアが低いほどリスク高・関連度が高いほどリスク増幅
  const gap = Math.max(0, 60 - score);          // 0〜60
  const relMulti = relevance ? 1 + matchCount * 0.2 : 0.4; // 関連なしでもゼロにしない
  const riskScore = Math.min(100, Math.round(gap / 60 * 100 * relMulti));
  return { riskScore, relevance, matchCount };
}

/* リスクレベルの判定 */
function riskLevel(score){
  if(score >= 60) return {lv:'high', label:'高', cls:'rb-high', obcls:'kpi-ob-high', color:'#a82020'};
  if(score >= 30) return {lv:'mid',  label:'中', label2:'中', cls:'rb-mid', obcls:'kpi-ob-mid', color:'#b05000'};
  return               {lv:'low',  label:'低', cls:'rb-low', obcls:'kpi-ob-low', color:'#1e6e45'};
}

/* スコアバーの色 */
function scoreBarColor(score){
  if(score < 25) return '#a82020';
  if(score < 42) return '#b05000';
  return '#1e6e45';
}

/* メイン：KPIリスク分析データを構築 */
function buildKPIRiskAnalysis(scores, plan){
  const kpis = (plan.kpis||[]).slice(0, 5); // 最大5KPI
  if(!kpis.length) return [];

  return kpis.map(kpiText => {
    const axes = CATS.map(c => {
      const {riskScore, relevance, matchCount} = calcAxisRisk(c.id, scores[c.id], kpiText);
      const lv = riskLevel(riskScore);
      const impact = KPI_AXIS_IMPACT[c.id];
      return {
        id:       c.id,
        name:     c.name,
        score:    scores[c.id],
        riskScore,
        relevance,
        matchCount,
        lv,
        reason:   impact.reasons[lv.lv],
        action:   impact.actions[lv.lv],
        color:    c.color,
      };
    });

    // KPI全体の総合リスクスコア（上位3軸の平均）
    const topAxes = [...axes].sort((a,b) => b.riskScore - a.riskScore).slice(0,3);
    const overallRiskScore = Math.round(topAxes.reduce((s,a) => s + a.riskScore, 0) / topAxes.length);
    const overallLv = riskLevel(overallRiskScore);

    return { kpiLabel: kpiText, axes, overallRiskScore, overallLv };
  });
}

