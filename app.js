/* メイン：フェーズ別達成シナリオを生成 */
function buildKPIScenarios(scores, plan){
  const kpis = (plan.kpis||[]).slice(0,5);
  if(!kpis.length) return null;

  return kpis.map(kpiText => {
    const type     = detectKPIType(kpiText);
    const category = detectKPICategory(kpiText);
    const baseline = estimateBaseline(scores, kpiText);

    // フェーズ別累積達成見込み
    const g1 = calcPhaseGain(scores, kpiText, 1);
    const g2 = calcPhaseGain(scores, kpiText, 2);
    const g3 = calcPhaseGain(scores, kpiText, 3);

    const p1 = Math.min(100, baseline + g1);
    const p2 = Math.min(100, p1 + g2);
    const p3 = Math.min(100, p2 + g3);

    // 主要施策の取得
    const phaseActions = PHASE_ACTIONS_BY_CATEGORY[category]
      || {1:'基盤整備・方針策定', 2:'AI活用の本格展開', 3:'全社最適化・定着'};

    // リスクレベルの算出（ベースラインが低いほどリスク高）
    const riskLv = baseline < 30 ? 'high' : baseline < 55 ? 'mid' : 'low';
    const riskLabels = {high:'達成困難', mid:'要継続投資', low:'達成見込あり'};
    const riskColors = {high:'#a82020',  mid:'#b05000',    low:'#1e6e45'};

    return {
      kpiText, type, category, baseline,
      phases:[
        {ph:1, label:'Phase 1完了時', pct:p1, gain:g1, action:phaseActions[1], color:'#d97706'},
        {ph:2, label:'Phase 2完了時', pct:p2, gain:g2, action:phaseActions[2], color:'#2563eb'},
        {ph:3, label:'Phase 3完了時', pct:p3, gain:g3, action:phaseActions[3], color:'#16a34a'},
      ],
      riskLv, riskLabel:riskLabels[riskLv], riskColor:riskColors[riskLv],
    };
  });
}

